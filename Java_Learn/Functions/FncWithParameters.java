package Java_Learn.Functions;

public class FncWithParameters {
    
    public static int sum(int a , int b) {   // Formal Parameters or Parameters
        return a + b;
    }
    public static void main(String[] args) {
        // Adding numbers 
        System.out.println(sum(4,5));  // Acutal Parameters or Arguments
        System.out.println(sum(4,5));  
        System.out.println(sum(87,15));  
        System.out.println(sum(84,574));  
    }
}
